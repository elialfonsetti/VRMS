    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            cc= ControllerFactoryClient.getInstance().getFornitoreInterface().getControllerCliente();
            cliente=cc.CreaCliente();
            cliente.setNome(jTextField1.getText());
            cliente.setCognome(jTextField3.getText());
            cliente.setTelefono(jTextField5.getText());
            cliente.setEmail(jTextField4.getText());
            cliente.setId_card(jTextField6.getText());
            String idt = jTextField7.getText();
            TesseraInterface tessera = cc.CreaTessera();
            tessera.setCodice(idt);
            tessera.setCredito(10);
            tessera.setAttiva(true);
            
            try {
                cc.NuovoCliente(cliente,tessera);
                cc.setCliente(cliente,ControllerFactoryClient.getInstance().getTerminale());
            } catch (Exception ex) {
                Logger.getLogger(NuovoClienteView.class.getName()).log(Level.SEVERE, null, ex);
            }
            PagePanel.activeView("RiepilogoNuovoClienteView");
        } catch (RemoteException ex) {
            Logger.getLogger(NuovoClienteView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(NuovoClienteView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(NuovoClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

