    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            TesseraInterface tessera;
            tessera = cliente.getTessera_personale();
            float nuovo_credito = tessera.getCredito() + Float.valueOf(jTextField5.getText());
            tessera.setCredito(nuovo_credito);
           
            try {
                cc.ModificaTessera(tessera);
                cc.setCliente(cliente,ControllerFactoryClient.getInstance().getTerminale());
            } catch (Exception ex) {
                Logger.getLogger(RicaricaTesseraView.class.getName()).log(Level.SEVERE, null, ex);
            }
            PagePanel.activeView("RiepilogoRicaricaTesseraView");
        } catch (RemoteException ex) {
            Logger.getLogger(RicaricaTesseraView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

