    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            cliente = cc.CercaCliente(jTextField7.getText());
            if(cliente != null){
                jLabel9.setText("");
                jLabel5.setText(cliente.getNome());
                jLabel10.setText(cliente.getCognome());
                jLabel11.setText(String.valueOf(cliente.getTessera_personale().getCredito()));
                
               
                jButton2.setEnabled(true);
            }
            else{
                jLabel9.setForeground(Color.red);
                jLabel9.setText("Tessera non riconosciuta!");
            }
        } catch (Exception ex) {
            Logger.getLogger(RicaricaTesseraView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

