    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            if (cc.CheckNoleggi(ControllerFactoryClient.getInstance().getTerminale())) {
                jLabel12.setText("");
                TesseraInterface tessera;
                tessera = cliente.getTessera_personale();
                tessera.setAttiva(false);
         
                try {
                    cc.ModificaTessera(tessera);
                } catch (Exception ex) {
                    Logger.getLogger(EliminaClienteView.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            else
            {    jLabel12.setForeground(Color.red);
                 jLabel12.setText("ATTENZIONE! Impossibile disattivare la tessera, sono presenti noleggi aperti associati al cliente");
            }
        } catch (Exception ex) {
            Logger.getLogger(EliminaClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }
        PagePanel.activeView("RiepilogoEliminaClienteView");
    }//GEN-LAST:event_jButton2ActionPerformed

